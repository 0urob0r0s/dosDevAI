#!/usr/bin/env python3
"""Build a glyph-fingerprint ‚Üí char map for the 86Box text-mode decoder.

Renders the standard VGA 8x16 BIOS font (codepage 437) using PIL and the
DejaVu mono fallback if the real BIOS font isn't available. For each
character code 0..255, draws an 8x16 (or 9x16) glyph on black, fingerprints
it the same way `screen.py` does, and writes the table to fontmap.py.

For best fidelity with 86Box's actual VGA output we should fingerprint
glyphs against a screenshot of the real running emulator showing a known
calibration pattern. That's a follow-up; this initial map gets us 80%+ of
character recognition for ASCII.

Usage:
    build-fontmap.py [--out fontmap.py] [--cell 8x16]
"""
